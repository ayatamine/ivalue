    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        try{
            $user_id = Auth::user()->id;
            $estates = Estate::where('user_id' , $user_id)->active()->get();
            $report_ids = [];
            foreach ($estates as $estate){
                foreach ($estate->reports as $report){
                    array_push($report_ids , $report->id);
                }
            }
            $reports = Report::whereIn('id',$report_ids)->orderBy('id','desc')->get();
            return view('frontend.reports.index', compact('reports'));
        }catch (\Exception $exception){
            return redirect()->back()->with('error', 'حدث خطأ !!');
        }
    }

